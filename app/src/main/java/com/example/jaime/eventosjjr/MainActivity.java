    public void generarNotificacion (int id) {
        NotificationCompat.Builder not = new NotificationCompat.Builder(this);
        not.setSmallIcon(android.R.drawable.stat_sys_warning);
        not.setContentTitle("Control Paciente");
        not.setSubText("Recordatorio");
        not.setContentText("Tiene Medicamentos pendientes de Juan Perez");
        not.setContentInfo("1");
        //Hacer que vibre el celular al aparecer la puta notificacion
        long[] velocidad = {0,100,200,300};
        not.setVibrate(velocidad);
        Intent notificacion = new Intent(this,MainActivity.class);
        PendingIntent intentoPendiente = PendingIntent.getActivity(this,0,notificacion,PendingIntent.FLAG_UPDATE_CURRENT);
        not.setFullScreenIntent(intentoPendiente,true);
        not.setSound(RingtoneManager.getDefaultUri(RingtoneManager.TYPE_NOTIFICATION));
        not.setContentIntent(intentoPendiente);
        nm = (NotificationManager) getSystemService(Context.NOTIFICATION_SERVICE);
        nm.notify(id,not.build());

    }


